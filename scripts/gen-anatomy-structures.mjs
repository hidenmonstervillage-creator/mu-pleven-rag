/**
 * scripts/gen-anatomy-structures.mjs
 *
 * Regenerates lib/anatomy-structures.ts — a map of distinctive anatomical
 * structure tokens → ["<model>|<group>", ...], used by lib/anatomy-match to
 * resolve specific structure names (e.g. "brachioradialis") from a chat question
 * to a model + group. Run whenever the GLBs in public/models change:
 *   node scripts/gen-anatomy-structures.mjs
 *
 * Generic/positional words are excluded; laterality (.r/.l) stripped. Node 18+.
 */
import { readFileSync, readdirSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const DIR = resolve(ROOT, 'public/models');
const OUT = resolve(ROOT, 'lib/anatomy-structures.ts');

function parseGlb(buf) {
  if (buf.readUInt32LE(0) !== 0x46546c67) throw new Error('bad magic');
  let o = 12;
  while (o < buf.length) {
    const l = buf.readUInt32LE(o), t = buf.readUInt32LE(o + 4);
    if (t === 0x4e4f534a) return JSON.parse(buf.subarray(o + 8, o + 8 + l).toString('utf8'));
    o += 8 + l;
  }
  throw new Error('no json');
}

const STOP = new Set(`muscle muscles bone bones nerve nerves nerver artery arteries arteryr vein veins veinr
cartilage cartilages ligament ligaments fascia fasciae joint joints branch branches branchr tendon tendons
capsule capsules sheath sheaths bursa bursae membrane membranes overlay overlays synovial synovia
right left superior inferior anterior posterior lateral medial deep superficial common proper external internal
proximal distal dorsal palmar plantar volar first second third fourth fifth sixth seventh eighth ninth tenth
longus brevis major minor magnus of the and with for from into over sub supra inter intra head neck body region
part parts wall canal cavity process arch arches horn root roots trunk trunks plexus septum area line group
cutaneous articular annular collateral transverse oblique straight accessory recurrent perforating`.split(/\s+/).filter(Boolean));

function cleanName(raw) { return raw.replace(/_/g, ' ').replace(/\.(r|l)$/i, '').trim(); }
function tokens(name) {
  return cleanName(name).toLowerCase()
    .normalize('NFD').replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9]+/g, ' ').trim().split(' ')
    .filter((w) => w.length >= 4 && !STOP.has(w));
}

const map = new Map();
for (const f of readdirSync(DIR).filter((x) => x.endsWith('.glb')).sort()) {
  const model = f.replace(/\.glb$/, '');
  const g = parseGlb(readFileSync(`${DIR}/${f}`));
  const nodes = g.nodes ?? [];
  const scene = g.scenes?.[g.scene ?? 0] ?? g.scenes[0];
  for (const ri of scene.nodes) {
    const group = nodes[ri]?.name; if (!group) continue;
    const stack = [ri];
    while (stack.length) {
      const i = stack.pop(); const n = nodes[i]; if (!n) continue;
      if (n.mesh !== undefined && n.name) {
        for (const tk of tokens(n.name)) {
          if (!map.has(tk)) map.set(tk, new Set());
          map.get(tk).add(`${model}|${group}`);
        }
      }
      for (const c of (n.children ?? [])) stack.push(c);
    }
  }
}

const obj = {};
for (const [k, set] of [...map.entries()].sort((a, b) => a[0].localeCompare(b[0]))) obj[k] = [...set].slice(0, 3);
const count = Object.keys(obj).length;

const header = `// AUTO-GENERATED by scripts/gen-anatomy-structures.mjs (do not edit by hand).
// Distinctive anatomical structure tokens -> ["<model>|<group>", ...].
// Used by lib/anatomy-match to resolve specific structure names (e.g.
// "brachioradialis") to a model + group topic. Generic/positional words are
// excluded; laterality (.r/.l) stripped; ${count} tokens.
export const STRUCTURE_TOKENS: Record<string, string[]> = ${JSON.stringify(obj, null, 0)};
`;
writeFileSync(OUT, header);
console.log(`wrote ${OUT} — ${count} tokens`);
